class MapTile(pygame.sprite.Sprite):
    level = [
        "----------------------------",
        "----------------------------",
        "----------------------------",
        "-............--............-",
        "-.----.-----.--.-----.----.-",
        "-,----.-----.--.-----.----,-",
        "-.----.-----.--.-----.----.-",
        "-..........................-",
        "-.----.--.--------.--.----.-",
        "-.----.--.--------.--.----.-",
        "-......--....--....--......-",
        "------.----- -- -----.------",
        "------.----- -- -----.------",
        "------.--    --    --.------",
        "------.--          --.------",
        "------.-- ---  --- --.------",
        "------.-- -      - --.------",
        "------.   -      -   .------",
        "------.-- -      - --.------",
        "------.-- -------- --.------",
        "------.--          --.------",
        "------.-- -------- --.------",
        "------.-- -------- --.------",
        "-............--............-",
        "-.----.-----.--.-----.----.-",
        "-,----.-----.--.-----.----,-",
        "-...--................--...-",
        "---.--.--.--------.--.--.---",
        "---.--.--.--------.--.--.---",
        "-......--....--....--......-",
        "-.----------.--.----------.-",
        "-.----------.--.----------.-",
        "-..........................-",
        "----------------------------"]

    @classmethod
    def reload(self):

        self.level = [
            "----------------------------",
            "----------------------------",
            "----------------------------",
            "-............--............-",
            "-.----.-----.--.-----.----.-",
            "-,----.-----.--.-----.----,-",
            "-.----.-----.--.-----.----.-",
            "-..........................-",
            "-.----.--.--------.--.----.-",
            "-.----.--.--------.--.----.-",
            "-......--....--....--......-",
            "------.----- -- -----.------",
            "------.----- -- -----.------",
            "------.--    --    --.------",
            "------.--          --.------",
            "------.-- ---  --- --.------",
            "------.-- -      - --.------",
            "------.   -      -   .------",
            "------.-- -      - --.------",
            "------.-- -------- --.------",
            "------.--          --.------",
            "------.-- -------- --.------",
            "------.-- -------- --.------",
            "-............--............-",
            "-.----.-----.--.-----.----.-",
            "-,----.-----.--.-----.----,-",
            "-...--................--...-",
            "---.--.--.--------.--.--.---",
            "---.--.--.--------.--.--.---",
            "-......--....--....--......-",
            "-.----------.--.----------.-",
            "-.----------.--.----------.-",
            "-..........................-",
            "----------------------------"]

    @classmethod
    def show_map(self):
        return self.level

    @classmethod
    def update(self, x, y):
        cool = 0
        if (x % const.BLOCK_WIDTH == 0) and (y % const.BLOCK_HEIGHT == 0):
            xt, yt = x // const.BLOCK_WIDTH, y // const.BLOCK_HEIGHT
            if self.level[yt][xt] != '-':
                if self.level[yt][xt] == '.':
                    cool = 5
                elif self.level[yt][xt] == ',':
                    cool = 100
                self.level[yt] = self.level[yt][:xt] + " " + self.level[yt][xt + 1:]
        return cool
    @classmethod
    def get_pixel(cls, x, y):
        if cls.level[y // const.BLOCK_HEIGHT][x // const.BLOCK_WIDTH] == '-':
            return True
        else:
            return False

